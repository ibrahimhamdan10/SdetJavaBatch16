package training;

public class Switch {
    public static void main(String[] args) {

        int day=4;
        switch (day){
            case 1:
                System.out.printf("sunday");
                break;
            case 2:
                System.out.printf("monday");
                break;
            case 3:
                System.out.printf("tuesday");
                break;
            case 4:
                System.out.printf("wed");
                break;
            default:
                System.out.println("nothing");


        }

    }
}
